// basic while loop structure
let i = 0;
while (i < 20) {
  i++;
  console.log(i); // 1-20 output
}

// basic while loop odd numbers

let oddNum = 1;
while (oddNum < 20) {
  console.log(oddNum); // output 1-3-5-7...19
  oddNum += 2;
}

// basic while loop even numbers
let evenNum = 0;
while (evenNum <= 20) {
  console.log(evenNum); // output 0-2-4-6...20
  evenNum += 2;
}
