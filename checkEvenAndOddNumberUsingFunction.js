// check even numbers  
function isEvenNumber(numbers) {
  if (numbers % 2 == 0) {
    return true;
  }
  return false;
}
console.log(isEvenNumber(44));

// check odd numbers  
function isOddNumber(numbers) {
    if (numbers % 2 != 0) {
      return true;
    }
    return false;
  }
  console.log(isOddNumber(45));